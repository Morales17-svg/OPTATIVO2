print ("------Ejercicio 3 Clase 1------")

print ("\nIngrese cuatro notas para calcular el promedio")

nota1 = float(input("\nIngrese la nota 1: "))
nota2 = float(input("\nIngrese la nota 2: "))
nota3 = float(input("\nIngrese la nota 3: "))
nota4 = float(input("\nIngrese la nota 4: "))

promedio = (nota1 + nota2 + nota3 + nota4) / 4

print (f"\nEl promedio de las notas, {nota1}, {nota2}, {nota3}, {nota4}: {promedio}")